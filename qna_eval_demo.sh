#!/usr/bin/env bash
# =============================================================================
# qna_eval_demo.sh
#
# Runs 3 "ask" questions against potpie_cli.py, collects the Q&A pairs into
# a YAML eval-cases file, then runs the "eval" command against that file.
#
# Usage:
#   ./qna_eval_demo.sh <project-id>
#
# Example:
#   ENABLE_MULTI_AGENT=false ./qna_eval_demo.sh 3e052a9f-63c0-0201-f730-ba90decbb97d
# =============================================================================

set -euo pipefail

# ---------------------------------------------------------------------------
# 1. Argument validation
# ---------------------------------------------------------------------------
if [[ $# -lt 1 ]]; then
    echo "Usage: $0 <project-id>" >&2
    exit 1
fi

PROJECT_ID="$1"

# Directory where this script lives (so it works from any cwd)
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CLI="$SCRIPT_DIR/potpie_cli.py"

# Output files
CASES_YAML="$SCRIPT_DIR/qna_eval_cases.yaml"

# Base command – honour any env vars already set by the caller, but default
# ENABLE_MULTI_AGENT to false to keep things fast and deterministic.
export ENABLE_MULTI_AGENT="${ENABLE_MULTI_AGENT:-false}"

PY_CMD="${PYTHON:-python}"   # override with e.g. PYTHON=python3 ./qna_eval_demo.sh …

# ---------------------------------------------------------------------------
# 2. Helper: ask one question and return just the answer text
# ---------------------------------------------------------------------------
ask_question() {
    local question="$1"
    # --no-markdown keeps the output as plain text (no Rich markup sequences).
    # We capture everything after the "Answer:" separator line.
    "$PY_CMD" "$CLI" ask "$question" -p "$PROJECT_ID" --no-markdown 2>/dev/null \
        | awk '/🤖 Answer:/{found=1; next} found && /^$/{if(started) next; next} found{started=1; print}' \
        | sed '/^[[:space:]]*$/{ /./!d }' \
        | sed "s/'/''/" \
        | sed 's/\\/\\\\/g'
}

# ---------------------------------------------------------------------------
# 3. Define the three questions + their evaluation rubrics
# ---------------------------------------------------------------------------
declare -a QUESTIONS=(
    "Explain the overall architecture of this codebase"
    "What is the main entry point of the application and how does it start up?"
    "What are the main external libraries or frameworks used in this project?"
)

declare -a NAMES=(
    "architecture overview"
    "entry point"
    "key dependencies"
)

# Rubrics are parallel arrays of pipe-separated strings (one per question)
declare -a RUBRICS=(
    "Answer is specific to the actual codebase, not a generic description|Answer mentions at least two concrete components, modules, or layers from the project"
    "Answer identifies a specific file or function as the entry point|Answer does not just say 'I don't know' or ask for clarification"
    "Answer lists at least two real dependencies from the project|Answer does not hallucinate libraries not present in the project"
)

# ---------------------------------------------------------------------------
# 4. Ask each question and build the YAML cases file
# ---------------------------------------------------------------------------
echo "============================================================"
echo " Potpie QnA Eval Demo"
echo " Project ID : $PROJECT_ID"
echo " Cases file : $CASES_YAML"
echo "============================================================"
echo ""

# Start the YAML file
cat > "$CASES_YAML" <<'YAML_HEADER'
# Auto-generated by qna_eval_demo.sh
# Contains Q&A cases with rubrics for LLM-as-a-judge evaluation.
cases:
YAML_HEADER

for i in "${!QUESTIONS[@]}"; do
    QUESTION="${QUESTIONS[$i]}"
    NAME="${NAMES[$i]}"
    RUBRIC_STR="${RUBRICS[$i]}"

    echo "[$((i+1))/${#QUESTIONS[@]}] Asking: \"$QUESTION\""

    # Run the ask command
    ANSWER="$("$PY_CMD" "$CLI" ask "$QUESTION" -p "$PROJECT_ID" --no-markdown 2>/dev/null \
        | awk '/🤖 Answer:/{found=1; next} found{print}' \
        | sed '/^[[:space:]]*$/{ N; /^\n[[:space:]]*$/d }' \
        | head -c 4000 \
        | sed "s/\"/'/g")"

    echo "   ✅ Answer received (${#ANSWER} chars)"
    echo ""

    # Write the case entry to YAML
    # Use a Python heredoc-style literal block scalar (|) for the answer
    {
        printf '  - name: "%s"\n' "$NAME"
        printf '    question: "%s"\n' "$QUESTION"
        printf '    answer: |\n'
        # Indent every line of the answer by 6 spaces for the YAML literal block
        echo "$ANSWER" | sed 's/^/      /'
        printf '    rubrics:\n'
        # Split rubrics on | and emit each as a list item
        IFS='|' read -ra RUBRIC_ITEMS <<< "$RUBRIC_STR"
        for rubric in "${RUBRIC_ITEMS[@]}"; do
            printf '      - "%s"\n' "$rubric"
        done
        printf '\n'
    } >> "$CASES_YAML"
done

echo "------------------------------------------------------------"
echo " Q&A collection complete. Cases written to:"
echo "   $CASES_YAML"
echo "------------------------------------------------------------"
echo ""

# ---------------------------------------------------------------------------
# 5. Run the eval command against the generated cases file
# ---------------------------------------------------------------------------
echo "Running eval command…"
echo ""

"$PY_CMD" "$CLI" eval \
    --project "$PROJECT_ID" \
    --agent   codebase_qna_agent \
    --cases   "$CASES_YAML"

echo ""
echo "============================================================"
echo " Done! Results above."
echo "============================================================"
